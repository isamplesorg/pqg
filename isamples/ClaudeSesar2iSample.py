    def create_sampling_event(self, sample_obj: Dict) -> Optional[SamplingEvent]:
        """Create sampling event from sample data."""
        event_pid = f"urn:local:evt.{sample_obj['sample_id']}"

        # Build collection description
        desc_parts = []

        # Collection method
        if sample_obj.get('collection_method_id'):
            method_key = f"sam.{sample_obj['collection_method_id']}"
            if method_key in self.concept_lkup:
                concept = self.concept_lkup[method_key]['obj']
                if not self.concept_lkup[method_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[method_key]['flag'] = True
                desc_parts.append(f"method:{concept['label']}; pid:{concept['pid']}")

        if sample_obj.get('collection_method_detail'):
            desc_parts.append(sample_obj['collection_method_detail'])

        # Platform
        if sample_obj.get('platform_id'):
            platform_key = f"pla.{sample_obj['platform_id']}"
            if platform_key in self.concept_lkup:
                concept = self.concept_lkup[platform_key]['obj']
                if not self.concept_lkup[platform_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[platform_key]['flag'] = True
                desc_parts.append(f"Platform: {concept['label']}; pid:{concept['pid']}")

        # Launch platform
        if sample_obj.get('launch_platform_id'):
            launch_key = f"pla.{sample_obj['launch_platform_id']}"
            if launch_key in self.concept_lkup:
                concept = self.concept_lkup[launch_key]['obj']
                if not self.concept_lkup[launch_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[launch_key]['flag'] = True
                desc_parts.append(f"Launch Platform: {concept['label']}; pid:{concept['pid']}")

        if sample_obj.get('launch_label'):
            desc_parts.append(f"Launch: {sample_obj['launch_label']}")

        collection_desc = "; ".join(desc_parts) if desc_parts else None

        # Get sampled feature type (context category)
        context_category = None
        if sample_obj.get('sampled_feature_type_id'):
            sft_key = f"sft.{sample_obj['sampled_feature_type_id']}"
            if sft_key in self.concept_lkup:
                concept = self.concept_lkup[sft_key]['obj']
                if not self.concept_lkup[sft_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[sft_key]['flag'] = True
                context_category = concept

        # Get collectors
        collectors = []
        collector_key = f"sam.{sample_obj['sample_id']}"
        if collector_key in self.collector_lkup:
            for collector_id in self.collector_lkup[collector_key]:
                if collector_id in self.agent_lkup:
                    agent = self.agent_lkup[collector_id]['obj']
                    self.agent_lkup[collector_id]['role'] = 'collector'
                    collectors.append(agent)

                    if not self.agent_lkup[collector_id]['flag']:
                        self.add_agent(agent)
                        self.agent_lkup[collector_id]['flag'] = True

        # Get locality name
        locality_name = None
        loc_key = f"loc.{sample_obj.get('locality_id')}"
        if loc_key in self.locality_lkup:
            locality_name = self.locality_lkup[loc_key].get('label')

        # Build time string
        time_str = None
        start_date = sample_obj.get('collection_start_date')
        end_date = sample_obj.get('collection_end_date')

        if start_date and end_date:
            time_str = f"{start_date}/{end_date}"
        elif start_date:
            time_str = str(start_date)
        elif end_date:
            time_str = str(end_date)

        # Get project/initiative
        project = None
        if sample_obj.get('cruise_field_prgrm_id'):
            init_key = f"urn:local:ini.{sample_obj['cruise_field_prgrm_id']}"
            if init_key in self.init_lkup:
                project = self.init_lkup[init_key]

        # Build label
        label_parts = []
        if project:
            label_parts.append(project)

        if sample_obj.get('launch_label'):
            label_parts.append(sample_obj['launch_label'])
        elif sample_obj.get('name'):
            label_parts.append(f"Collection of sample {sample_obj['name']}")
        else:
            label_parts.append("Collection of sample")

        if time_str:
            label_parts.append(time_str)

        label = "; ".join(label_parts)

        # Create sampling event record
        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', 'SamplingEvent')
        insert_val['pid'] = event_pid
        insert_val['description'] = collection_desc
        insert_val['authorized_by'] = None
        insert_val['has_feature_of_interest'] = locality_name
        insert_val['label'] = label
        insert_val['project'] = project
        insert_val['result_time'] = time_str
        self.batch_processor.add(insert_val)

        # Create and link geospatial location
        geo_location = self.create_geospatial_location(sample_obj)
        if geo_location:
            self.add_edge(event_pid, 'sample_location', geo_location['pid'])

        # Create and link sampling site
        sampling_site = self.create_sampling_site(sample_obj)
        if sampling_site:
            self.add_edge(event_pid, 'sampling_site', sampling_site['pid'])

        # Link collectors
        for collector in collectors:
            self.add_edge(event_pid, 'responsibility', collector['pid'])

        # Link context category
        if context_category:
            self.add_edge(event_pid, 'has_context_category', context_category['pid'])

        return SamplingEvent(
            pid=event_pid,
            description=collection_desc,
            authorized_by=None,
            has_context_category=context_category,
            has_feature_of_interest=locality_name,
            label=label,
            project=project,
            responsibility=collectors,
            result_time=time_str,
            sample_location=geo_location,
            sampling_site=sampling_site
        )


