class DatabaseManager:
    """Manages database connections and operations."""

    def __init__(self, config: SESARConfig):
        self.config = config
        self._sesar_conn = None
        self._duck_conn = None
        self._graph = None

    @contextmanager
    def sesar_connection(self):
        """Context manager for SESAR PostgreSQL connection."""
        try:
            logger.info(f'set up db connection: {self.config.db_name}')
            conn = psycopg2.connect(
                database=self.config.db_name,
                user=self.config.db_user,
                password=self.config.db_password,
                host=self.config.db_host,
                port=self.config.db_port,
            )
            yield conn
        except Exception as e:
            logger.error(f'SESAR database connection failed: {e}')
            raise
        finally:
            if conn:
                conn.close()

    def get_duckdb_graph(self):
        """Get or create DuckDB graph connection."""
        if self._duck_conn is None:
            import duckdb
            self._duck_conn = duckdb.connect(self.config.duckdb_path)
            self._graph = createGraph(self._duck_conn)
        return self._graph

    def execute_query(self, conn, query: str) -> List[Tuple]:
        """Execute query and return results."""
        with conn.cursor() as cursor:
            cursor.execute(query)
            return cursor.fetchall()

    def get_fields(self, conn, table_name: str) -> List[str]:
        """Get field names for a table."""
        query = """
                SELECT column_name
                FROM information_schema.columns
                WHERE table_schema = 'public' \
                  AND table_name = %s
                ORDER BY ordinal_position \
                """
        try:
            results = self.execute_query(conn, query % f"'{table_name}'")
            return [row[0] for row in results]
        except Exception as e:
            logger.error(f"Error getting fields for {table_name}: {e}")
            return []

    def close(self):
        """Close all connections."""
        if self._duck_conn:
            self._duck_conn.close()


