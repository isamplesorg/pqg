class SESARDataLoader:
    """Main class for loading SESAR data."""

    def __init__(self, config: SESARConfig):
        self.config = config
        self.db_manager = DatabaseManager(config)
        self.cache_manager = CacheManager(config.cache_dir)

        # Lookups
        self.concept_lkup: Dict = {}
        self.agent_lkup: Dict = {}
        self.sesar_user_lkup: Dict = {}
        self.init_lkup: Dict = {}
