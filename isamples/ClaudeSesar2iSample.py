    def process_sample(self, sample_obj: Dict):
        """Process a single sample record."""
        sample_id = sample_obj['sample_id']
        sample_pid = f"sam.{sample_id}"

        # Get registrant
        registrant = None
        if sample_obj.get('cur_registrant_id'):
            reg_key = str(sample_obj['cur_registrant_id'])
            if reg_key in self.sesar_user_lkup:
                agent_key = self.sesar_user_lkup[reg_key]
                if agent_key in self.agent_lkup:
                    registrant = self.agent_lkup[agent_key]['obj']
                    registrant['role'] = 'registrant'

                    if not self.agent_lkup[agent_key]['flag']:
                        self.add_agent(registrant)
                        self.agent_lkup[agent_key]['flag'] = True

        # Build description
        desc_parts = []
        if sample_obj.get('sample_description'):
            desc_parts.append(sample_obj['sample_description'])

        if sample_obj.get('material_name_verbatim'):
            desc_parts.append(f"Material: {sample_obj['material_name_verbatim']}")

        if sample_obj.get('size'):
            desc_parts.append(f"Sample size: {sample_obj['size']}")

        if sample_obj.get('latitude') and sample_obj.get('latitude_end'):
            desc_parts.append(
                f"Latitude from {sample_obj['latitude']} to {sample_obj['latitude_end']}"
            )

        if sample_obj.get('longitude') and sample_obj.get('longitude_end'):
            desc_parts.append(
                f"Longitude from {sample_obj['longitude']} to {sample_obj['longitude_end']}"
            )

        if sample_obj.get('geologic_age_verbatim'):
            desc_parts.append(f"Age: {sample_obj['geologic_age_verbatim']}")

        # Numeric ages
        age_min = sample_obj.get('numeric_age_min')
        age_max = sample_obj.get('numeric_age_max')

        if age_min and age_max:
            desc_parts.append(f"Age between {age_min} and {age_max}")
        elif age_min:
            desc_parts.append(f"Minimum age {age_min}")
        elif age_max:
            desc_parts.append(f"Maximum age {age_max}")

        if sample_obj.get('numeric_age_unit'):
            desc_parts.append(f"Age units {sample_obj['numeric_age_unit']}")

        if sample_obj.get('age_qualifier'):
            desc_parts.append(f"Age qualifier: {sample_obj['age_qualifier']}")

        description = ". ".join(desc_parts) if desc_parts else None

        # Build keywords
        keywords = []

        if sample_obj.get('geologic_unit'):
            geo_concept = IdentifiedConcept(
                pid=f"geo_unit.{sample_id}",
                label=sample_obj['geologic_unit'],
                scheme_name="Geologic Unit",
                scheme_uri=None
            )
            keywords.append(geo_concept)
            self.add_concept(geo_concept)

        # Geologic age older
        if sample_obj.get('geologic_age_older_id'):
            older_key = f"gts.{sample_obj['geologic_age_older_id']}"
            if older_key in self.concept_lkup:
                concept = self.concept_lkup[older_key]['obj']
                if not self.concept_lkup[older_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[older_key]['flag'] = True
                keywords.append(concept)

        # Geologic age younger
        if sample_obj.get('geologic_age_younger_id'):
            younger_key = f"gts.{sample_obj['geologic_age_younger_id']}"
            if younger_key in self.concept_lkup:
                concept = self.concept_lkup[younger_key]['obj']
                if not self.concept_lkup[younger_key]['flag']:
                    self.add_concept(concept)
                    self.concept_lkup[younger_key]['flag'] = True
                keywords.append(concept)

        # Get additional names
        alt_names = self.addname_lkup.get(str(sample_id))

        # Get related resources
        related_rels = self.relres_lkup.get(str(sample_id))
        if related_rels:
            for rel in related_rels:
                insert_val = self.get_blank_insert()
                self.set_value(insert_val, 'otype', 'SampleRelation')
                insert_val['pid'] = rel['pid']
                insert_val['target'] = rel['target']
                insert_val['description'] = ''
                insert_val['label'] = rel['label']
                insert_val['relationship'] = 'has parent material sample'
                self.batch_processor.add(insert_val)

        # Get category concepts
        context_cat = None
        if sample_obj.get('sampled_feature_type_id'):
            sft_key = f"sft.{sample_obj['sampled_feature_type_id']}"
            if sft_key in self.concept_lkup:
                context_cat = self.concept_lkup[sft_key]['obj']
                if not self.concept_lkup[sft_key]['flag']:
                    self.add_concept(context_cat)
                    self.concept_lkup[sft_key]['flag'] = True

        material_cat = None
        if sample_obj.get('general_material_type_id'):
            mat_key = f"mat.{sample_obj['general_material_type_id']}"
            if mat_key in self.concept_lkup:
                material_cat = self.concept_lkup[mat_key]['obj']
                if not self.concept_lkup[mat_key]['flag']:
                    self.add_concept(material_cat)
                    self.concept_lkup[mat_key]['flag'] = True

        sample_obj_type = None
        if sample_obj.get('sample_type_id'):
            sat_key = f"sat.{sample_obj['sample_type_id']}"
            if sat_key in self.concept_lkup:
                sample_obj_type = self.concept_lkup[sat_key]['obj']
                if not self.concept_lkup[sat_key]['flag']:
                    self.add_concept(sample_obj_type)
                    self.concept_lkup[sat_key]['flag'] = True

        # Create curation
        curation = self.create_curation(sample_obj)

        # Create sampling event
        sampling_event = self.create_sampling_event(sample_obj)

        # Create material sample record
        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', 'MaterialSampleRecord')
        insert_val['pid'] = sample_pid
        insert_val['alternate_identifiers'] = alt_names
        insert_val['complies_with'] = None
        insert_val['dc_rights'] = None
        insert_val['description'] = description
        insert_val['label'] = sample_obj.get('name')
        insert_val['last_modified_time'] = str(sample_obj.get('last_update_date', ''))
        insert_val['sample_identifier'] = sample_obj.get('igsn')
        insert_val['sampling_purpose'] = sample_obj.get('purpose')
        self.batch_processor.add(insert_val)

        # Add edges
        if curation:
            self.add_edge(sample_pid, 'curation', curation['pid'])

        if context_cat:
            self.add_edge(sample_pid, 'has_context_category', context_cat['pid'])

        if material_cat:
            self.add_edge(sample_pid, 'has_material_category', material_cat['pid'])

        if sample_obj_type:
            self.add_edge(sample_pid, 'has_sample_object_type', sample_obj_type['pid'])

        for keyword in keywords:
            self.add_edge(sample_pid, 'keywords', keyword['pid'])

        if sampling_event:
            self.add_edge(sample_pid, 'produced_by', sampling_event['pid'])

        if registrant:
            self.add_edge(sample_pid, 'registrant', registrant['pid'])

        if related_rels:
            for rel in related_rels:
                self.add_edge(sample_pid, 'related_resource', rel['pid'])


    def load_samples(self):
        """Load all samples from database."""
        logger.info("Starting sample load")

        with self.db_manager.sesar_connection() as conn:
            # Build query
            where_clause = ""
            if self.config.is_update:
                last_sync = datetime.now().date() - timedelta(days=self.config.update_window_days)
                last_sync_str = last_sync.strftime('%Y-%m-%d 00:00:00')
                where_clause = f"WHERE last_update_date >= '{last_sync_str}'"

            query = f"SELECT * FROM sample {where_clause}"

            # Get fields
            fields = self.db_manager.get_fields(conn, 'sample')

            # Use cursor for batch fetching
            with conn.cursor() as cursor:
                cursor.execute(query)

                samples_processed = 0

                with self.batch_processor.processing_context():
                    while True:
                        # Fetch batch
                        batch = cursor.fetchmany(self.config.batch_size)

                        if not batch:
                            logger.info("No more samples to process")
                            break

                        # Process each sample
                        for row in batch:
                            sample_obj = dict(zip(fields, row))
                            self.process_sample(sample_obj)
                            samples_processed += 1

                        logger.info(f"Processed {samples_processed} samples")

        logger.info(f"Sample load complete. Total: {samples_processed}")


