    def load_vocabularies(self):
        """Load all vocabularies."""
        logger.info("Loading vocabularies")

        with self.db_manager.sesar_connection() as conn:
            for vocab_config in VOCAB_CONFIGS:
                self._load_concept_vocabulary(conn, vocab_config)

            # Load material types (special case)
            self._load_material_types(conn)

        logger.info(f"Loaded {len(self.concept_lkup)} concepts")

    def _load_concept_vocabulary(self, conn, config: List):
        """Load a single vocabulary table."""
        table_name, abbrev, uri_field, label_field, scheme_name, scheme_uri_field, id_field = config

        try:
            query = f"SELECT * FROM {table_name}"
            data = self.db_manager.execute_query(conn, query)
            fields = self.db_manager.get_fields(conn, table_name)

            for row in data:
                obj = dict(zip(fields, row))

                # Determine PID
                if uri_field and obj.get(uri_field):
                    pid = obj[uri_field]
                else:
                    pid = f"urn:local:{abbrev}.{obj[id_field]}"

                # Determine scheme URI
                scheme_uri = obj.get(scheme_uri_field) if scheme_uri_field else ''

                # Create concept
                concept = IdentifiedConcept(
                    pid=pid,
                    label=obj[label_field],
                    scheme_name=scheme_name,
                    scheme_uri=scheme_uri,
                    altids=[f"{abbrev}.{obj[id_field]}"]
                )

                lookup_key = f"{abbrev}.{obj[id_field]}"
                self.concept_lkup[lookup_key] = {'flag': False, 'obj': concept}

            logger.info(f"Loaded vocabulary: {table_name}")

        except Exception as e:
            logger.error(f"Error loading vocabulary {table_name}: {e}")

    def _load_material_types(self, conn):
        """Load material types (special handling)."""
        try:
            query = """
                    SELECT material_type.material_type_id, \
                           material_type.label,
                           scheme_uri, \
                           material_type_uri
                    FROM material_type
                             JOIN sample ON material_type_id = general_material_type_id
                    UNION
                    SELECT material_type.material_type_id, \
                           material_type.label,
                           scheme_uri, \
                           material_type_uri
                    FROM material_type
                             JOIN sample_material ON material_type.material_type_id = sample_material.material_type_id \
                    """

            data = self.db_manager.execute_query(conn, query)

            for row in data:
                mat_id, label, scheme_uri, mat_uri = row

                pid = mat_uri if mat_uri else f"urn:local:mat.{mat_id}"

                concept = IdentifiedConcept(
                    pid=pid,
                    label=label,
                    scheme_name="SESAR Material Type",
                    scheme_uri=scheme_uri or '',
                    altids=[f"mat.{mat_id}"]
                )

                self.concept_lkup[f"mat.{mat_id}"] = {'flag': False, 'obj': concept}

            logger.info("Loaded material types")

        except Exception as e:
            logger.error(f"Error loading material types: {e}")

