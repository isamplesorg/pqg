    def get_edge_pid(self, s: str, p: str, o: str, n: Optional[str] = None) -> str:
        """Generate edge PID from subject, predicate, object."""
        try:
            h = hashlib.md5()
            h.update(s.encode("utf-8"))
            h.update(p.encode("utf-8"))
            h.update(o.encode("utf-8"))
            if n is not None:
                h.update(n.encode("utf-8"))
            return f"anon_{h.hexdigest()}"
        except Exception as e:
            logger.error(f"Error generating edge PID: {e}")
            return None


    def add_edge(self, s: str, p: str, o: str, n: Optional[str] = None):
        """Add an edge to the batch."""
        edge_pid = self.get_edge_pid(s, p, o, n)
        if not edge_pid:
            return

        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', '_edge_')
        insert_val['pid'] = edge_pid
        insert_val['s'] = s
        insert_val['p'] = p
        insert_val['o'] = o
        insert_val['n'] = n
        self.batch_processor.add(insert_val)


