class CacheManager:
    """Manages pickle cache files for lookups."""

    def __init__(self, cache_dir: str = "cache"):
        self.cache_dir = pathlib.Path(cache_dir)
        self.cache_dir.mkdir(exist_ok=True)

    def get_cache_path(self, name: str) -> pathlib.Path:
        return self.cache_dir / f"{name}.pkl"

    def load_or_create(self, name: str, create_func) -> Optional[Dict]:
        """Load from cache or create new."""
        cache_path = self.get_cache_path(name)
        print ("load cache from", cache_path)
        try:
            if cache_path.exists():
                with open(cache_path, 'rb') as f:
                    data = pickle.load(f)
                    logger.info(f"Loaded {name} from cache")
                    return data
        except Exception as e:
            logger.warning(f"Failed to load cache {name}: {e}")

        # Create new
        try:
            data = create_func()
            if data:
                with open(cache_path, 'wb') as f:
                    pickle.dump(data, f)
                logger.info(f"Created and cached {name}")
                return data
            else:
                logger.error(f"Failed to create {name}")
                return None
        except Exception as e:
            logger.error(f"Error creating {name}: {e}")
            return None


