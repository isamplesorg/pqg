    def run(self):
        """Main execution method."""
        start_time = time.time()

        try:
            # Initialize
            self.initialize()

            # Load or create vocabularies
            cached_concepts = self.cache_manager.load_or_create(
                'concept_lkup',
                self.load_vocabularies
            )
            if cached_concepts:
                self.concept_lkup = cached_concepts

            # Load or create agents
            cached_agents = self.cache_manager.load_or_create(
                'agent_lkup',
                self.load_agents
            )
            if cached_agents:
                self.agent_lkup = cached_agents

            # Load supporting lookups
            self.load_supporting_lookups()

            # Load samples
            self.load_samples()

            # Export to Parquet
            if self.config.output_parquet:
                graph = self.db_manager.get_duckdb_graph()
                output_path = pathlib.Path(self.config.output_parquet)
                graph.asParquet(output_path)
                logger.info(f"Exported to Parquet: {output_path}")

            # Print stats
            elapsed = time.time() - start_time
            logger.info(f"Total execution time: {elapsed / 3600:.2f} hours")
            logger.info(f"Batch stats: {self.batch_processor.stats}")

        except Exception as e:
            logger.error(f"Error during execution: {e}", exc_info=True)
            raise
        finally:
            self.db_manager.close()


