    def create_geospatial_location(self, sample_obj: Dict) -> Optional[GeospatialCoordLocation]:
        """Create geospatial coordinate location from sample data."""
        lat = sample_obj.get('latitude')
        lon = sample_obj.get('longitude')
        elev = sample_obj.get('elevation')
        depth_min = sample_obj.get('depth_min')
        depth_max = sample_obj.get('depth_max')

        # Check if we have any location data
        if not any([lat, lon, elev, depth_min, depth_max]):
            return None

        # Build vertical position string
        vert_parts = []
        if elev is not None:
            vert_parts.append(str(elev))
            if sample_obj.get('elevation_uom'):
                vert_parts.append(f"UOM: {sample_obj['elevation_uom']}")

        if depth_min is not None or depth_max is not None:
            if depth_min == depth_max:
                vert_parts.append(f"Depth: {depth_min}")
            elif depth_min and depth_max:
                vert_parts.append(f"Depth range: {depth_min} to {depth_max}")
            elif depth_min:
                vert_parts.append(f"Depth minimum: {depth_min}")
            elif depth_max:
                vert_parts.append(f"Depth maximum: {depth_max}")

            if sample_obj.get('depth_uom'):
                vert_parts.append(f"UOM: {sample_obj['depth_uom']}")

        vertical_pos = " ".join(vert_parts) if vert_parts else None

        # Create geometry if we have coordinates
        geometry = None
        if isinstance(lat, numbers.Number) and isinstance(lon, numbers.Number):
            try:
                point_wkt = f"POINT({lat} {lon})"
                geometry = str(wkt.loads(point_wkt))
            except Exception as e:
                logger.warning(f"Failed to create geometry for sample {sample_obj.get('sample_id')}: {e}")

        # Create location record
        pid = f"urn:local:geo.{sample_obj['sample_id']}"

        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', 'GeospatialCoordLocation')
        insert_val['pid'] = pid
        insert_val['latitude'] = str(lat) if lat is not None else None
        insert_val['longitude'] = str(lon) if lon is not None else None
        insert_val['obfuscated'] = False
        insert_val['elevation'] = vertical_pos
        insert_val['geometry'] = geometry
        self.batch_processor.add(insert_val)

        return GeospatialCoordLocation(
            pid=pid,
            latitude=str(lat) if lat is not None else None,
            longitude=str(lon) if lon is not None else None,
            obfuscated=False,
            elevation=vertical_pos
        )


    def create_sampling_site(self, sample_obj: Dict) -> Optional[SamplingSite]:
        """Create sampling site from sample data."""
        site_pid = f"urn:local:sst.{sample_obj['sample_id']}"

        # Build description
        desc_parts = []
        if sample_obj.get('locality_detail'):
            desc_parts.append(sample_obj['locality_detail'])

        if sample_obj.get('latitude_end'):
            desc_parts.append(
                f"Sampling from lat, long {sample_obj.get('latitude')}, "
                f"{sample_obj.get('longitude')} to {sample_obj['latitude_end']}, "
                f"{sample_obj.get('longitude_end')}"
            )

        if sample_obj.get('location_qualifier'):
            desc_parts.append(sample_obj['location_qualifier'])

        description = ". ".join(desc_parts) if desc_parts else None

        # Get place names from locality
        place_names = []
        loc_name = None

        try:
            locality_key = f"loc.{sample_obj['locality_id']}"
            if locality_key in self.locality_lkup:
                locality = self.locality_lkup[locality_key]

                for field in ['province', 'county', 'city']:
                    if locality.get(field):
                        place_names.append(locality[field])

                if locality.get('country_id'):
                    country_key = f"cty.{locality['country_id']}"
                    if country_key in self.concept_lkup:
                        place_names.append(self.concept_lkup[country_key]['obj']['label'])

                loc_name = locality.get('name')
        except Exception as e:
            logger.debug(f"Error getting locality info: {e}")

        # Create site record
        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', 'SamplingSite')
        insert_val['pid'] = site_pid
        insert_val['description'] = description
        insert_val['label'] = loc_name
        insert_val['place_name'] = place_names if place_names else None
        insert_val['is_part_of'] = None
        self.batch_processor.add(insert_val)

        return SamplingSite(
            pid=site_pid,
            description=description,
            label=loc_name,
            place_name=place_names,
            site_location=None,
            is_part_of=None
        )


