    def _load_individuals(self, conn):
        """Load individuals into agent lookup."""
        try:
            query = "SELECT * FROM individual"
            data = self.db_manager.execute_query(conn, query)
            fields = self.db_manager.get_fields(conn, 'individual')

            for row in data:
                obj = dict(zip(fields, row))

                # Determine label
                label = (obj.get('label') or obj.get('individual_uri') or
                         obj.get('description') or obj.get('email'))

                if not label:
                    continue

                # Determine PID
                pid = obj.get('individual_uri') or f"urn:local:ind.{obj['individual_id']}"

                # Build contact info
                contact_parts = []
                if obj.get('address'):
                    contact_parts.append(obj['address'])
                if obj.get('email'):
                    contact_parts.append(obj['email'])
                if obj.get('phone'):
                    contact_parts.append(f"Phone: {obj['phone']}")

                agent = Agent(
                    pid=pid,
                    affiliation=None,
                    contact_information=", ".join(contact_parts),
                    name=label,
                    role='',
                    altids=[f"ind.{obj['individual_id']}"]
                )

                self.agent_lkup[f"ind.{obj['individual_id']}"] = {
                    'flag': False,
                    'obj': agent
                }

            logger.info("Loaded individuals")

        except Exception as e:
            logger.error(f"Error loading individuals: {e}")

