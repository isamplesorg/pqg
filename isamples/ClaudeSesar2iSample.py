from enum import Enum


class FlushStrategy(Enum):
    SIZE = "size"
    TIME = "time"
    HYBRID = "hybrid"


@dataclass
class BatchStats:
    items_processed: int = 0
    items_failed: int = 0
    batches_flushed: int = 0
    total_flush_time: float = 0.0
    start_time: float = time.time()

    @property
    def elapsed_time(self) -> float:
        return time.time() - self.start_time

    @property
    def items_per_second(self) -> float:
        elapsed = self.elapsed_time
        return self.items_processed / elapsed if elapsed > 0 else 0.0


class BatchProcessor:
    def __init__(
            self,
            batch_size: int = 1000,
            flush_callback=None,
            strategy: FlushStrategy = FlushStrategy.SIZE,
            max_retries: int = 3,
    ):
        self.batch_size = batch_size
        self.flush_callback = flush_callback
        self.strategy = strategy
        self.max_retries = max_retries

        self._batch: List[Dict[str, Any]] = []
        self._last_flush_time = time.time()
        self.stats = BatchStats()

    def add(self, item: Dict[str, Any]) -> None:
        try:
            self._batch.append(item)
            self.stats.items_processed += 1

            if len(self._batch) >= self.batch_size:
                self.flush()

        except Exception as e:
            self.stats.items_failed += 1
            logger.error(f"Error adding item to batch: {e}", exc_info=True)

    def flush(self) -> None:
        if not self._batch or not self.flush_callback:
            return

        flush_start = time.time()
        retry_count = 0

        while retry_count <= self.max_retries:
            try:
                self.flush_callback(self._batch)
                self.stats.batches_flushed += 1
                self.stats.total_flush_time += time.time() - flush_start
                self._last_flush_time = time.time()
                self._batch.clear()
                logger.info(f"Batch flushed. Stats: {self.stats.items_processed} processed, "
                            f"{self.stats.items_per_second:.2f} items/sec")
                break

            except Exception as e:
                retry_count += 1
                logger.error(f"Error flushing batch (attempt {retry_count}/{self.max_retries}): {e}")

                if retry_count > self.max_retries:
                    logger.error(f"Max retries exceeded. Losing {len(self._batch)} items!")
                    self.stats.items_failed += len(self._batch)
                    self._batch.clear()
                    raise
                else:
                    time.sleep(2 ** retry_count)

    def finalize(self) -> BatchStats:
        if self._batch:
            self.flush()
        return self.stats

    @contextmanager
    def processing_context(self):
        try:
            yield self
        finally:
            self.finalize()


