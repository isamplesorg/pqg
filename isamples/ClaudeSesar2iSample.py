    def create_curation(self, sample_obj: Dict) -> Optional[MaterialSampleCuration]:
        """Create material sample curation record."""
        if not sample_obj.get('cur_owner_id'):
            return None

        owner_key = str(sample_obj['cur_owner_id'])
        if owner_key not in self.sesar_user_lkup:
            return None

        agent_key = self.sesar_user_lkup[owner_key]
        if agent_key not in self.agent_lkup:
            return None

        owner = self.agent_lkup[agent_key]['obj']
        owner['role'] = 'owner'

        if not self.agent_lkup[agent_key]['flag']:
            self.add_agent(owner)
            self.agent_lkup[agent_key]['flag'] = True

        curation_pid = f"urn:local:cur.{sample_obj['sample_id']}"

        # Create curation record
        insert_val = self.get_blank_insert()
        self.set_value(insert_val, 'otype', 'MaterialSampleCuration')
        insert_val['pid'] = curation_pid
        insert_val['label'] = owner['name']
        insert_val['description'] = 'current owner'
        insert_val['access_constraints'] = []
        self.batch_processor.add(insert_val)

        # Link owner to curation
        self.add_edge(curation_pid, 'responsibility', owner['pid'])

        return MaterialSampleCuration(
            pid=curation_pid,
            responsibility=owner,
            label=owner['name'],
            description='current owner',
            access_constraints=[]
        )


